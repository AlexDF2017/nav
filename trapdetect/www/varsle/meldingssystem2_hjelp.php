<p>Velg de kategoriene du vil abonnere på ved å klikke i boksene.</p>

<p>Merking er lagt opp på en tilnærmet hierarkisk måte. Hvis du bare
krysser av for en kategori velger du alle enhetene som er i den
kategorien. Hvis du krysser av for en kategori og deretter en
underkategori, betyr det at du IKKE vil ha med underkategorien. Hvis
du i tillegg krysser av en enhet som er under underkategorien, legger
du den TIL.</p>

<p>Hvis du er usikker er det bare å prøve og trykke <b>Gå videre</b>
for å se hvilke enheter som er valgt. Hvis det er feil, trykker du på
<b>Tilbake</b> knappen i browseren og velger på nytt.</p>

<p>NB! Når det gjelder listene er det er forskjellige måter å velge på
ettersom hvilken browser du har. I Netscape er det bare å klikke. I
Opera og Internet Explorer må du trykke inn CTRL+Venstre musknapp for
å velge flere enheter eller velge vekk den du har valgt.</p>

